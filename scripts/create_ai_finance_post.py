#!/usr/bin/env python3
"""
Generate AI Finance Visual for Instagram
Creates professional finance/AI themed images
"""

import matplotlib.pyplot as plt
import matplotlib.patches as patches
import numpy as np
from datetime import datetime
import os

def create_finance_ai_visual(output_path="/tmp/ai_finance_visual.png"):
    """Create a professional AI Finance visual"""
    
    # Set up figure
    plt.figure(figsize=(10, 8), facecolor='#0f172a')
    ax = plt.gca()
    ax.set_facecolor('#0f172a')
    
    # Title
    plt.suptitle('AI + Finance Revolution', 
                fontsize=28, fontweight='bold', color='#60a5fa', y=0.95)
    
    # Subtitle
    plt.title('Claude Code • Trading Algorithms • Financial Automation',
             fontsize=16, color='#94a3b8', pad=20)
    
    # Create 3 main sections
    sections = [
        {
            'title': '🤖 AI Trading',
            'points': [
                '• Algorithmic Strategies',
                '• Real-time Analysis',
                '• Risk Management',
                '• Portfolio Optimization'
            ],
            'color': '#3b82f6',
            'position': (0.15, 0.6)
        },
        {
            'title': '💻 Claude Code',
            'points': [
                '• Financial Scripts',
                '• API Integrations',
                '• Data Pipelines',
                '• Automation Tools'
            ],
            'color': '#10b981',
            'position': (0.5, 0.6)
        },
        {
            'title': '📊 GitHub Repos',
            'points': [
                '• Trading Bots',
                '• Data Analysis',
                '• Risk Models',
                '• Visualization'
            ],
            'color': '#8b5cf6',
            'position': (0.85, 0.6)
        }
    ]
    
    # Draw sections
    for section in sections:
        x, y = section['position']
        
        # Draw circle
        circle = patches.Circle((x, y), 0.08, 
                               facecolor=section['color'] + '20',
                               edgecolor=section['color'],
                               linewidth=2)
        ax.add_patch(circle)
        
        # Section title
        plt.text(x, y + 0.12, section['title'],
                fontsize=14, fontweight='bold',
                color='white', ha='center')
        
        # Points
        for i, point in enumerate(section['points']):
            plt.text(x, y - 0.02 - i*0.05, point,
                    fontsize=10, color='#cbd5e1',
                    ha='center')
    
    # Bottom info
    plt.text(0.5, 0.25, 'Follow for daily AI Finance insights',
            fontsize=12, color='#94a3b8', ha='center')
    
    # Hashtags
    hashtags = '#AIFinance #TradingAlgorithms #ClaudeCode #GitHub #FinancialAI #Automation #FinTech'
    plt.text(0.5, 0.18, hashtags,
            fontsize=11, color='#60a5fa', ha='center')
    
    # Footer
    footer_text = f'Generated by AI • {datetime.now().strftime("%Y-%m-%d")}'
    plt.text(0.5, 0.05, footer_text,
            fontsize=10, color='#64748b', ha='center')
    
    # Remove axes
    ax.set_xticks([])
    ax.set_yticks([])
    ax.spines['top'].set_visible(False)
    ax.spines['right'].set_visible(False)
    ax.spines['bottom'].set_visible(False)
    ax.spines['left'].set_visible(False)
    
    # Adjust layout
    plt.tight_layout(rect=[0, 0.05, 1, 0.95])
    
    # Save
    plt.savefig(output_path, dpi=300, bbox_inches='tight', facecolor='#0f172a')
    plt.close()
    
    print(f"✅ Created AI Finance visual: {output_path}")
    return output_path

