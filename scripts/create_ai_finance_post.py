def create_instagram_post_script(image_path, caption):
    """Create script to post to Instagram"""
    
    script = f"""#!/bin/bash
# Instagram Posting Script for AI Finance Content
# Generated: {datetime.now().isoformat()}

echo "📱 Posting AI Finance content to Instagram"
echo "=========================================="

# Check if logged in
echo "🔍 Checking Instagram login status..."
pinchtab text > /tmp/insta_status.json
URL=$(jq -r '.url' /tmp/insta_status.json 2>/dev/null)

if [[ "$URL" != *"instagram.com"* ]] || [[ "$URL" == *"login"* ]]; then
    echo "❌ Not logged into Instagram"
    echo "   Please log in first: pinchtab nav https://www.instagram.com/"
    exit 1
fi

echo "✅ Logged into Instagram"

# Navigate to create page
echo "➕ Going to create post page..."
pinchtab nav "https://www.instagram.com/create/select/"
sleep 3

# Check if we're on create page
pinchtab snap -c > /tmp/create_snapshot.txt
if grep -q "Create" /tmp/create_snapshot.txt; then
    echo "✅ On create post page"
else
    echo "⚠️  Might need manual navigation to create page"
    echo "   Looking for create button..."
    
    # Try to find create button
    pinchtab snap -c | grep -i "create\\|plus\\|add\\|post" | head -5
    echo "   Please click create button manually if needed"
fi

echo ""
echo "📊 POST DETAILS:"
echo "  Image: {image_path}"
echo "  Caption length: {len(caption)} characters"
echo ""
echo "📝 CAPTION PREVIEW:"
echo "{caption[:200]}..."
echo ""
echo "⚠️  IMPORTANT:"
echo "   This script demonstrates the workflow."
echo "   Actual posting requires element mapping for:"
echo "   1. Upload button click"
echo "   2. Image selection"
echo "   3. Caption input"
echo "   4. Share button click"
echo ""
echo "✅ Ready for posting once elements are mapped!"

# Save caption to file for reference
echo "$caption" > /tmp/instagram_caption.txt
echo "📄 Caption saved to: /tmp/instagram_caption.txt"
"""
    
    return script

if __name__ == "__main__":
    print("🎨 Generating AI Finance visual...")
    image_path = create_finance_ai_visual()
    
    print("\n📝 Generating caption...")
    caption = generate_caption()
    
    print("\n📱 Creating Instagram posting script...")
    script_content = create_instagram_post_script(image_path, caption)
    
    # Save script
    script_path = "/tmp/post_ai_finance.sh"
    with open(script_path, "w") as f:
        f.write(script_content)
    
    os.chmod(script_path, 0o755)
    
    print(f"\n✅ COMPLETE!")
    print(f"   Visual: {image_path}")
    print(f"   Script: {script_path}")
    print(f"   Caption: /tmp/instagram_caption.txt")
    
    # Show image info
    import subprocess
    result = subprocess.run(["file", image_path], capture_output=True, text=True)
    print(f"\n📸 Image info: {result.stdout.strip()}")